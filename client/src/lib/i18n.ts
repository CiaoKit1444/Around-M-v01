const LOCALE_STORAGE_KEY = "pa_guest_locale";

function detectBrowserLocale(): Locale {
  const lang = navigator.language?.split("-")[0]?.toLowerCase();
  const supported = LOCALES.map((l) => l.code);
  return (supported.includes(lang as Locale) ? lang : "en") as Locale;
}

let _locale: Locale = (() => {
  try {
    const stored = localStorage.getItem(LOCALE_STORAGE_KEY) as Locale | null;
    if (stored && LOCALES.some((l) => l.code === stored)) return stored;
    return detectBrowserLocale();
  } catch {
    return "en";
  }
})();

const listeners = new Set<() => void>();

export function setLocale(locale: Locale) {
  _locale = locale;
  try { localStorage.setItem(LOCALE_STORAGE_KEY, locale); } catch { /* ignore */ }
  listeners.forEach((fn) => fn());
}

import { useState, useEffect, useCallback } from "react";

export function useI18n() {
  const [locale, setLocaleState] = useState<Locale>(_locale);

  useEffect(() => {
    const handler = () => setLocaleState(_locale);
    listeners.add(handler);
    return () => { listeners.delete(handler); };
  }, []);

  const t = useCallback((key: string, fallback?: string): string => {
    return translations[locale]?.[key] ?? translations.en[key] ?? fallback ?? key;
  }, [locale]);

  const changeLocale = useCallback((l: Locale) => {
    setLocale(l);
    setLocaleState(l);
  }, []);

  const isRTL = LOCALES.find((l) => l.code === locale)?.rtl ?? false;

  return { t, locale, setLocale: changeLocale, isRTL };
}
